// 数组解构是按照顺序解构,对象解构则是按照属性名解构
let {a, b} = {b: 1, a: 2};
console.log(a);
console.log(b);

// 没有对应的解构属性名的属性,取不到值赋予undefined
let {c} = {a: 1};
console.log(c);

// 解构属性名和属性不一样时,可以采用 解构属性名:属性 来对应取得
// 这种写法即为上边写法的完全体,上边写法为简写.
let {e: d} = {e: 1};
console.log(d);
// 对象的解构本质是找到同名的属性,把解构到的值赋给该属性对应的变量. 属性名仅作为匹配用,不是变量.
